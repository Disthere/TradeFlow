using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;
using TradeFlow.Domain.Common;
using TradeFlow.Domain.Enums;

namespace TradeFlow.Domain.Entities
{
    public class Position : Entity
    {
        public string Ticker { get; private set; }
        public decimal Quantity { get; private set; }
        public decimal AveragePrice { get; private set; }
        // При создании позиции реализованной прибыли еще нет
        public decimal TotalRealizedPnL { get; private set; }

        public Position(string ticker, decimal quantity, decimal averagePrice)
        {
            if (string.IsNullOrWhiteSpace(ticker))
                throw new ArgumentException("Ticker cannot be empty.", nameof(ticker));
            if (quantity < 0)
                throw new ArgumentException("Quantity cannot be negative.", nameof(quantity));
            if (averagePrice < 0)
                throw new ArgumentException("Average price cannot be negative.", nameof(averagePrice));

            Ticker = ticker.ToUpper();
            Quantity = quantity;
            AveragePrice = quantity == 0 ? 0 : averagePrice;

            // При создании позиции PnL всегда равен 0
            TotalRealizedPnL = 0m;
        }

        public void UpdatePosition(decimal executionQty, decimal executionPrice, OrderSide side) // 
        {
            if (executionQty <= 0)
                throw new ArgumentException("Quantity must be positive.", nameof(executionQty));
            if (executionPrice <= 0)
                throw new ArgumentException("Price must be positive.", nameof(executionPrice));

            var realizedPnL = executionQty * executionPrice;

            if (side == OrderSide.Buy)
            {
                // Пересчитываем среднюю цену покупки по формуле средневзвешенного значения
                Quantity += executionQty;
                var totalCost = (Quantity * AveragePrice) + (executionQty * executionPrice);
                AveragePrice = totalCost / Quantity;
                // При покупке TotalRealizedPnL НЕ МЕНЯЕТСЯ
            }
            else if (side == OrderSide.Sell)
            {
                if (Quantity < executionQty)
                    throw new InvalidOperationException("Not enough shares to sell");

                // Считаем реальную прибыль/убыток от этой сделки
                // Формула: (Цена продажи - Средняя цена покупки) * Количество
                decimal tradePnL = executionQty * (executionPrice - AveragePrice);

                // Прибавляем прибыль (или вычитаем убыток, если tradePnL отрицательный)
                TotalRealizedPnL += tradePnL;

                // При продаже средняя цена оставшихся акций НЕ меняется (только уменьшается количество)
                Quantity -= executionQty;
                
                if (Quantity == 0)
                    AveragePrice = 0;
            }
        }
    }
}

/*
 Этот класс представляет собой **доменную модель финансовой позиции** (например, пакета акций или криптовалюты) в портфеле трейдера или инвестиционного приложения. 

Его главная задача — надежно хранить данные о позиции и **самостоятельно** управлять их изменением при совершении сделок (покупке или продаже), гарантируя математическую и логическую корректность данных.

Вот подробный разбор того, что делает этот класс и какие принципы программирования в нем применены:

---

### 1. Свойства и Инкапсуляция
```csharp
public string Ticker { get; private set; }
public decimal Quantity { get; private set; }
public decimal AveragePrice { get; private set; }
```
*   **Что делает:** Хранит тикер актива (например, "AAPL"), текущее количество и среднюю цену покупки.
*   **Принцип:** Используется `private set`. Это означает, что внешний код **не может** напрямую изменить эти значения (например, написать `position.Quantity = -5`). Изменить состояние можно только через конструктор или метод `UpdatePosition`. Это защищает данные от случайной порчи (Инкапсуляция).

### 2. Конструктор и Валидация (Fail-Fast)
```csharp
public Position(string ticker, decimal quantity, decimal averagePrice)
```
*   **Что делает:** Создает новую позицию и сразу проверяет входные данные.
*   **Принцип Fail-Fast (Быстрый отказ):** Класс сразу выбрасывает исключения, если данные некорректны (пустой тикер, отрицательное количество или цена). Это не позволяет создать объект в невалидном состоянии.
*   **Нормализация:** `Ticker = ticker.ToUpper();` гарантирует, что тикер всегда будет в верхнем регистре ("aapl" превратится в "AAPL"), что упрощает дальнейшее сравнение и поиск.

### 3. Метод `UpdatePosition` (Бизнес-логика)
Это "сердце" класса. Он обновляет позицию при исполнении ордера.

#### А) Валидация сделки:
```csharp
if (executionQty <= 0) ...
if (executionPrice <= 0) ...
```
Гарантирует, что размер сделки и цена строго положительны.

#### Б) Логика покупки (`OrderSide.Buy`):
```csharp
var totalCost = (Quantity * AveragePrice) + (executionQty * executionPrice);
Quantity += executionQty;
AveragePrice = totalCost / Quantity;
```
*   **Что делает:** Пересчитывает **средневзвешенную цену** (Average Cost Basis). 
*   **Пример:** У вас было 10 акций по $100 (всего $1000). Вы купили еще 10 акций по $200 (всего $2000). 
    *   Новая общая стоимость: $1000 + $2000 = $3000.
    *   Новое количество: 10 + 10 = 20.
    *   Новая средняя цена: $3000 / 20 = **$150**.
    Класс делает этот расчет автоматически и безопасно.

#### В) Логика продажи (`OrderSide.Sell`):
```csharp
if (Quantity < executionQty) throw new InvalidOperationException(...);
Quantity -= executionQty;
```
*   **Что делает:** Уменьшает количество акций. 
*   **Важный момент:** Средняя цена (`AveragePrice`) при этом **не меняется**. Это корректно с точки зрения бухгалтерского учета: продажа части актива не меняет цену, по которой вы покупали оставшиеся акции.
*   **Защита инварианта:** Проверка `if (Quantity < executionQty)` запрещает продать больше акций, чем у вас есть (в данной реализации запрещены короткие позиции / шорт).

---

### Какие принципы хорошего кода здесь применены?
1. **Инкапсуляция:** Состояние объекта защищено от внешних изменений.
2. **Самодостаточность (Self-encapsulation):** Класс сам следит за своей целостностью. Внешнему сервису не нужно знать формулу расчета средней цены или проверять, не ушел ли баланс в минус.
3. **Использование `decimal`:** Для финансовых расчетов (цена, количество) используется тип `decimal`, а не `double` или `float`, чтобы избежать ошибок округления и потери точности.

---

### Чего не хватает в этом классе (потенциальные точки роста)?
Если это реальный продакшн-код, в будущем могут потребоваться доработки:
1. **Комиссии (Fees):** При покупке комиссия обычно *увеличивает* среднюю цену покупки, а при продаже *уменьшает* итоговую выручку. Сейчас комиссии не учитываются.
2. **Шорт-позиции (Short Selling):** Сейчас код выбросит ошибку при попытке продать больше, чем есть, или если начальное количество отрицательное. Для поддержки шорта логика потребуется усложнить.
3. **Реализованная прибыль (Realized PnL):** Метод `UpdatePosition` при продаже мог бы возвращать или сохранять в отдельное поле сумму реализованной прибыли/убытка от этой конкретной сделки.
4. **Полное закрытие позиции:** Если `Quantity` становится равной `0`, средняя цена математически не имеет смысла. Хорошей практикой может быть сброс `AveragePrice` в `0` при `Quantity == 0`.
 

Архитектурный совет (Best Practice)
В реальных торговых системах часто используют еще более строгий подход: объект Position с Quantity == 0 вообще не должен существовать.
Вместо того чтобы хранить его и обнулять поля, при полном закрытии позиции (Quantity == 0) сервис, который управляет портфелем, должен просто удалить этот объект из коллекции позиций (например, из Dictionary<string, Position> или List<Position>).

 
 В контексте трейдинга и финансовых систем execution (читается как «экзекьюшн», на русском часто говорят «исполнение», «сделка» или сленговое «филл» от англ. fill) — это фактическое совершение сделки на бирже по вашей заявке.
Это ключевое понятие, которое нужно отличать от Order (заявки/ордера).
Разница между Order и Execution
Order (Заявка/Ордер) — это ваше намерение.
Пример: «Я хочу купить 100 акций Apple по цене не выше $150».
Execution (Исполнение/Сделка) — это факт, когда биржа нашла контрагента и реально обменяла ваши деньги на акции.
Пример: Биржа купила для вас 100 акций, но по средней цене $150.05.
Почему в коде используются executionQty и executionPrice?
В методе UpdatePosition(decimal executionQty, decimal executionPrice, ...) переменные означают следующее:
executionQty (или fillQty) — количество активов, которые реально купили или продали в этой конкретной сделке.
executionPrice (или fillPrice) — цена, по которой биржа фактически провела эту сделку.
Почему это важно? (Два главных сценария)
1. Частичное исполнение (Partial Fill)
Вы отправили заявку (Order) на покупку 100 акций. Но на рынке в данный момент есть продавцы только на 60 акций.
Биржа совершит первое execution:
executionQty = 60
executionPrice = 150.00
Ваша заявка останется висеть на оставшиеся 40 акций. Позже может прийти второе execution на 40 акций по цене 150.10.
Ваш класс Position должен уметь обрабатывать каждое такое исполнение по отдельности, обновляя среднюю цену.
2. Проскальзывание цены (Slippage)
Вы выставили рыночный ордер (Market Order) на покупку по текущей цене, ожидая увидеть $150. Но пока ваш ордер летел до биржи, цена изменилась, и сделка состоялась по $150.20.
Ваша ожидаемая цена: $150
executionPrice (фактическая): $150.20
Именно executionPrice должна использоваться для расчета вашей реальной средней цены в позиции, а не та цена, которую вы "хотели".
Простая аналогия из жизни
Представьте, что вы делаете заказ в ресторане:
Order — это ваш заказ официанту: «Мне, пожалуйста, 2 стейка».
Execution — это момент, когда кухню реально приготовила и принесла вам эти стейки, и вам выставили финальный счет.
Если на кухне остался только один стейк, вам принесут сначала его (первое execution), а второй принесут позже (второе execution). А если цены в меню изменились, вам посчитают по той цене, которая действовала в момент фактического приготовления (фактического execution).
Резюме для вашего кода
Метод UpdatePosition специально назван так и принимает параметры execution..., чтобы подчеркнуть: позиция в портфеле должна обновляться только тогда, когда сделка реально состоялась на бирже, а не в момент, когда вы просто отправили заявку.
 
 
